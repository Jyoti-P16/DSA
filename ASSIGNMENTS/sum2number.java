import java.util.*;
public class sum2number {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter two numbers: ");
        int num1 = sc.nextInt();
        int num2 = sc.nextInt();
        System.out.println("Sum of two numbers = " + (num1 + num2));
    }
    
}
